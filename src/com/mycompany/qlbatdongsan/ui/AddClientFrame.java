    public String RandomMaKhachHang() {
        return "KH" + RanDomNumberAutomic();
    }

    public int RanDomNumberAutomic() {
        int a = 1;
        int b = 100;
        int Random;
        do {
            Random = ThreadLocalRandom.current().nextInt(a, b);
            for (KhachHang khachHang : listKhachHang) {
                int soKH = Integer.parseInt(khachHang.getMaKH().substring(2));
                if (Random == soKH) {
                    Random = -1;
                    a = a + 100;
                    b = b + 100;
                    break;
                }
            }
        } while (Random == -1);

        return Random;
    }

    public void addImage(File file) {
        TaiLieuDuAN image = new TaiLieuDuAN();
        image.setTenTaiLieu("Hình khách hàng: " + txtHoVaTenDem + txtTenKH);
        image.setDienGiai("Hình khách hàng");
        image.setHinh(file.getName());
        image.setMaDA(null);
        image.setMaKH(maKH);
        image.setMaSPDA(null);
        image.setNhanVien(Auth.user.getMaNV());
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        LocalDateTime now = LocalDateTime.now();
        image.setNgayCapNhat(XDate.toDate(dtf.format(now), "yyyy-MM-dd"));
        try {
            TaiLieuDuAN imageKH = daoImage.selectByIdmaKH(maKH);
            if (image.getMaKH() == imageKH.getMaKH()) {
                daoImage.deleteImageWithMAKH(maKH);
                daoImage.insert(image);
                return;
            }
            daoImage.insert(image);
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi lưu hình ảnh");
            e.printStackTrace();
        }
    }

    public File chonAnh() {
        File file;
        if (jFileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            file = jFileChooser.getSelectedFile();
            XImage.save(file);
            ImageIcon icon = XImage.read(file.getName());
            lblHinhAnh.setIcon(icon);
            lblHinhAnh.setToolTipText(file.getName());
            return file;
        }
        return null;
    }
